def config(key, default=None, cast=None):
    """Simple environment variable reader with type casting."""
    value = os.environ.get(key, default)
    if cast is not None and value is not None:
        if cast == bool:
            return str(value).lower() in ('true', '1', 'yes')
        return cast(value)
    return value

